#include <iostream>

using namespace std;

template<typename Item>
class MaxHeap {
private:
	Item *data;
	int count; // 堆中有多少个元素

public:
	// 构造函数, 构造一个空堆, 可容纳capacity个元素
	MaxHeap(int capacity) {
		data = new Item[capacity + 1];
		count = 0;
	}
	~MaxHeap() {
		delete[] data;
	}
	// 返回堆中的元素个数
	int size() {
		return count;
	}
	// 返回一个布尔值, 表示堆中是否为空
	bool isEmpty() {
		return count == 0;
	}
};
int main() {
	MaxHeap<int> maxheap = MaxHeap<int>(100);
	cout << maxheap.size() << endl;

	return 0;
}